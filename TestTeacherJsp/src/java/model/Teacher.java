package model;

/**
 *
 * @author Admin
 */
public class Teacher {
     
    private int id;
    private String name;
    private String dept;
    private String gender;

    public Teacher() {
    }

    public Teacher(int id, String name, String dept, String gender) {
        this.id = id;
        this.name = name;
        this.dept = dept;
        this.gender = gender;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDept() {
        return dept;
    }

    public void setDept(String dept) {
        this.dept = dept;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }
    
    
}
